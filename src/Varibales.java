public class Varibales {
    public static void main(String[] args) {
        // Primitive Data
        byte elMenorByte = Byte.MIN_VALUE;
        byte elMayorByte = Byte.MAX_VALUE;

        System.out.println("Lower byte " + elMayorByte + " More byte "+elMayorByte);
        System.out.println("Lower short " + Short.MIN_VALUE + " More short "+ Short.MAX_VALUE);
        System.out.println("Lower integer " + Integer.MIN_VALUE + " More integer "+ Integer.MAX_VALUE);
        System.out.println("Lower long " + Long.MIN_VALUE + " More long "+ Long.MAX_VALUE);

        float variableFloat = 5.6f;
        double variableDouble = 5.6;
        char variableChar = 'a';
        boolean variableBoolean = true;
    }
}
